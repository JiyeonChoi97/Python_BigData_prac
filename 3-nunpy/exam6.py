import numpy

# 모든 원소가 0 또는 1인 numpy 배열 생성
arr1 = numpy.zeros(10)
print(arr1)
print(arr1.dtype)
print(arr1.shape)
print('-' * 30)

arr1 = numpy.zeros((3, 4))
print(arr1)
print(arr1.dtype)
print(arr1.shape)
print('-' * 30)

arr1 = numpy.ones(10)
print(arr1)
print(arr1.dtype)
print(arr1.shape)
print('-' * 30)

arr1 = numpy.ones((3, 4))
print(arr1)
print(arr1.dtype)
print(arr1.shape)
print('-' * 30)






